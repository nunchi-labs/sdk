/// Deterministic identifier for a traded asset.
///
/// The CLOB keeps this generic so spot, perps, house, and batch-clearing clients
/// can agree on asset ids without coupling order matching to a settlement module.
#[derive(Clone, Copy, Debug, Eq, Hash, Ord, PartialEq, PartialOrd)]
pub struct AssetId(pub Digest);

/// Stable identifier for a base/quote market.
#[derive(Clone, Copy, Debug, Eq, Hash, Ord, PartialEq, PartialOrd)]
pub struct MarketId(pub Digest);

/// Stable identifier for an order accepted by the book.
#[derive(Clone, Copy, Debug, Eq, Hash, Ord, PartialEq, PartialOrd)]
pub struct OrderId(pub Digest);

/// Stable identifier for a match generated by the book.
#[derive(Clone, Copy, Debug, Eq, Hash, Ord, PartialEq, PartialOrd)]
pub struct FillId(pub Digest);

macro_rules! digest_id_codec {
    ($ty:ty) => {
        impl Write for $ty {
            fn write(&self, buf: &mut impl bytes::BufMut) {
                self.0.write(buf);
            }
        }

        impl Read for $ty {
            type Cfg = ();

            fn read_cfg(buf: &mut impl bytes::Buf, _: &Self::Cfg) -> Result<Self, Error> {
                Ok(Self(Digest::read(buf)?))
            }
        }

        impl FixedSize for $ty {
            const SIZE: usize = Digest::SIZE;
        }
    };
}

digest_id_codec!(AssetId);
digest_id_codec!(MarketId);
digest_id_codec!(OrderId);
digest_id_codec!(FillId);

/// Side of the book an order rests on.
#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub enum Side {
    Bid,
    Ask,
}

impl Side {
    pub fn opposite(self) -> Self {
        match self {
            Self::Bid => Self::Ask,
            Self::Ask => Self::Bid,
        }
    }

    pub fn crosses(self, taker_price: u128, maker_price: u128) -> bool {
        match self {
            Self::Bid => taker_price >= maker_price,
            Self::Ask => taker_price <= maker_price,
        }
    }
}

impl Write for Side {
    fn write(&self, buf: &mut impl bytes::BufMut) {
        match self {
            Self::Bid => 0_u8.write(buf),
            Self::Ask => 1_u8.write(buf),
        }
    }
}

impl Read for Side {
    type Cfg = ();

    fn read_cfg(buf: &mut impl bytes::Buf, _: &Self::Cfg) -> Result<Self, Error> {
        match u8::read(buf)? {
            0 => Ok(Self::Bid),
            1 => Ok(Self::Ask),
            tag => Err(Error::InvalidEnum(tag)),
        }
    }
}

impl EncodeSize for Side {
    fn encode_size(&self) -> usize {
        1
    }
}

