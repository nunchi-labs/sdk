/// Lifecycle state of an order.
#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub enum OrderStatus {
    Open,
    PartiallyFilled,
    Filled,
    Cancelled,
    Expired,
}

impl OrderStatus {
    pub fn is_open(self) -> bool {
        matches!(self, Self::Open | Self::PartiallyFilled)
    }
}

impl Write for OrderStatus {
    fn write(&self, buf: &mut impl bytes::BufMut) {
        match self {
            Self::Open => 0_u8.write(buf),
            Self::PartiallyFilled => 1_u8.write(buf),
            Self::Filled => 2_u8.write(buf),
            Self::Cancelled => 3_u8.write(buf),
            Self::Expired => 4_u8.write(buf),
        }
    }
}

impl Read for OrderStatus {
    type Cfg = ();

    fn read_cfg(buf: &mut impl bytes::Buf, _: &Self::Cfg) -> Result<Self, Error> {
        match u8::read(buf)? {
            0 => Ok(Self::Open),
            1 => Ok(Self::PartiallyFilled),
            2 => Ok(Self::Filled),
            3 => Ok(Self::Cancelled),
            4 => Ok(Self::Expired),
            tag => Err(Error::InvalidEnum(tag)),
        }
    }
}

impl EncodeSize for OrderStatus {
    fn encode_size(&self) -> usize {
        1
    }
}

/// Market metadata owned by the CLOB.
